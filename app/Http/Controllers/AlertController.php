    {
        $alerts = \App\Alert::join('tasks', 'tasks.id', '=', 'alerts.task_id')
            ->where('tasks.user_id', \Auth::user()->id)->with('node')->orderBy('alerts.created_at', 'desc')->get();
        return view('alert/index')->with('alerts', $alerts);
    }

